class WebhookHandler(BaseHTTPRequestHandler):
    mapper = None
    
    def do_POST(self):
        content_length = int(self.headers.get('Content-Length', 0))
        body = self.rfile.read(content_length).decode('utf-8')
        try:
            data = json.loads(body)
            event_type = data.get("event", data.get("type", ""))
            value = data.get("value", data.get("amount", 0))
            if self.mapper and event_type:
                self.mapper.handle(event_type, value, **data)
            self.send_response(200)
            self.send_header("Content-Type", "application/json")
            self.end_headers()
            self.wfile.write(b'{"ok":true}')
        except Exception as e:
            self.send_response(400)
            self.end_headers()
            self.wfile.write(str(e).encode())
    
    def do_GET(self):
        """Health check / status endpoint."""
        self.send_response(200)
        self.send_header("Content-Type", "application/json")
        self.end_headers()
        status = {
            "status": "running",
            "version": "2.1",
            "toys_connected": len(TOYS),
            "toys": list(TOYS.keys()),
            "events_supported": [
                "damage_taken", "damage_dealt", "hp_update", "death", "raise",
                "heal_received", "limit_break", "tankbuster", "boss_cast",
                "aoe_warning", "stack_marker", "enrage", "duty_complete", "wipe",
                "combat_start", "combat_end", "emote", "chat_trigger",
                "zone_change", "cutscene_start", "cutscene_end"
            ]
        }
        self.wfile.write(json.dumps(status, indent=2).encode())
    
    def log_message(self, format, *args):
        pass


