class EventMapper:
    def __init__(self, controller):
        self.ctrl = controller
        self.combo_counter = 0
        self.last_event = None
        self.last_event_time = 0
        self.player_hp_pct = 100
        self.in_combat = False
    
    def get_toy(self, name):
        """Get toy ID by name, case-insensitive."""
        return TOYS.get(name.lower(), TOYS.get(name, ""))
    
    def handle(self, event_type, value=0, **kwargs):
        now = time.time()
        if event_type == self.last_event and now - self.last_event_time < 2:
            self.combo_counter = min(self.combo_counter + 1, 8)
        else:
            self.combo_counter = 0
        self.last_event = event_type
        self.last_event_time = now
        combo = self.combo_counter * 2
        
        # Get first available toy for each slot
        all_toys = list(TOYS.values())
        toy1 = all_toys[0] if len(all_toys) > 0 else ""
        toy2 = all_toys[1] if len(all_toys) > 1 else ""
        toy3 = all_toys[2] if len(all_toys) > 2 else ""
        
        # ---- COMBAT ----
        if event_type == "damage_taken":
            damage = value
            max_hp = kwargs.get("max_hp", 50000)
            pct = min(100, (damage / max(1, max_hp)) * 100)
            intensity = min(20, max(3, int(pct / 5) + 3 + combo))
            print(f"  [COMBAT]  Damage: {damage} ({pct:.0f}% HP) -> intensity {intensity}")
            self.ctrl.vibrate(intensity, 2, toy1)
            if pct > 20:
                self.ctrl.vibrate(min(intensity - 3, 15), 2, toy2)
        
        elif event_type == "damage_dealt":
            intensity = min(8, max(2, value // 10000 + 2))
            print(f"  [HIT] Dealt: {value} -> {intensity}")
            self.ctrl.vibrate(intensity, 1, toy2)
        
        elif event_type == "hp_update":
            hp_pct = value
            self.player_hp_pct = hp_pct
            if hp_pct < 30:
                print(f"  [HP]  HP CRITICAL: {hp_pct}%")
                self.ctrl.vibrate(16 + combo, 3, toy1)
            elif hp_pct < 50:
                print(f"  [HP] HP LOW: {hp_pct}%")
                self.ctrl.vibrate(12, 3, toy1)
        
        elif event_type == "death":
            print(f"  [DEAD] DEATH -> full burst then silence")
            self.ctrl.vibrate(20, 1)
            time.sleep(1.5)
            self.ctrl.stop_all()
        
        elif event_type == "raise":
            print(f"  [UP] Raised!")
            self.ctrl.pattern("0;5;10;15;10;5;0;5;10;15;20", 5)
        
        elif event_type == "heal_received":
            intensity = min(10, max(2, value // 5000 + 2))
            print(f"  [HEAL] Healed: {value} -> gentle {intensity}")
            self.ctrl.pattern("3;5;7;5;3", 3, toy1)
        
        elif event_type == "limit_break":
            level = kwargs.get("level", 1)
            print(f"  [LB] LIMIT BREAK L{level} -> MAX POWER")
            self.ctrl.vibrate(20, 5 + level * 3)
        
        elif event_type == "tankbuster":
            print(f"  [TANK]  TANKBUSTER")
            self.ctrl.vibrate(20, 3, toy1)
            self.ctrl.vibrate(15, 3, toy2)
        
        elif event_type == "boss_cast":
            name = kwargs.get("name", "???")
            print(f"  [FIRE] Boss casting: {name}")
            self.ctrl.pattern("5;10;15;18;20;20;20", 5, toy1)
        
        elif event_type == "aoe_warning":
            print(f"  [AOE] AOE WARNING")
            self.ctrl.pattern("0;8;15;8;0;8;15;8;0", 3, toy1)
        
        elif event_type == "stack_marker":
            print(f"  [STACK] Stack marker!")
            self.ctrl.pattern("5;8;11;14;17;20", 3)
        
        elif event_type == "enrage":
            print(f"  [DEAD][FIRE] ENRAGE -> EVERYTHING MAX")
            for t in all_toys:
                self.ctrl.vibrate(20, 30, t)
        
        elif event_type == "duty_complete":
            print(f"  [WIN] DUTY COMPLETE!")
            self.ctrl.preset("fireworks", 15)
        
        elif event_type == "wipe":
            print(f"  [DEAD][DEAD] WIPE")
            self.ctrl.stop_all()
        
        elif event_type == "combat_start":
            self.in_combat = True
            print(f"  [COMBAT]  Combat started")
            self.ctrl.vibrate(3, 10, toy1)
        
        elif event_type == "combat_end":
            self.in_combat = False
            print(f"  [PEACE]  Combat ended")
            self.ctrl.pattern("5;3;2;1;0", 5)
        
        # ---- EMOTES ----
        elif event_type == "emote":
            emote = kwargs.get("name", "")
            emote_responses = {
                "hug":      ("[HUG]", "5;8;10;8;5;3", 5),
                "kiss":     ("[KISS]", "3;5;8;12;15;18;15;12;8;5", 8),
                "pet":      ("[PET]", "4;6;4;6;4", 4),
                "slap":     ("[SLAP]", None, 0),
                "spank":    ("[SPANK]", None, 0),
                "dance":    ("[DANCE]", None, 0),
                "dote":     ("[LOVE]", "5;8;10;12;10;8;5", 6),
                "comfort":  ("[HEART]", "3;5;7;5;3;5;7;5;3", 8),
                "blowkiss": ("[BLOWKISS]", "5;10;15;10;5", 5),
            }
            if emote in emote_responses:
                emoji, pat, dur = emote_responses[emote]
                print(f"  {emoji} Emote: /{emote}")
                if emote == "slap":
                    self.ctrl.vibrate(18, 1)
                elif emote == "spank":
                    self.ctrl.vibrate(20, 1)
                    time.sleep(1)
                    self.ctrl.pattern("0;15;0;15;0;15", 5)
                elif emote == "dance":
                    self.ctrl.preset("wave", 15)
                elif pat:
                    self.ctrl.pattern(pat, dur)
        
        # ---- ERP/CHAT ----
        elif event_type == "chat_trigger":
            ktype = kwargs.get("keyword_type", "mild")
            responses = {
                "mild":    (8,  "[HOT]",   "mild"),
                "intense": (15, "[HOT][HOT]",  "intense"),
                "climax":  (20, "[!!!]",   "CLIMAX"),
                "tease":   (10, "[TEASE]",   "tease"),
                "rough":   (18, "[HIT]",   "rough"),
            }
            if ktype in responses:
                intensity, emoji, label = responses[ktype]
                print(f"  {emoji} ERP: {label}")
                if ktype == "climax":
                    for t in all_toys:
                        self.ctrl.vibrate(20, 15, t)
                elif ktype == "tease":
                    self.ctrl.pattern("5;8;12;15;18;15;12;8;5;0;0;0;5;8;12;15;18;20", 15, toy1)
                elif ktype == "rough":
                    self.ctrl.preset("earthquake", 8, toy1)
                else:
                    self.ctrl.vibrate(min(20, intensity + combo), 5, toy1)
                    self.ctrl.vibrate(min(15, intensity // 2 + combo), 5, toy2)
        
        # ---- ZONE/MISC ----
        elif event_type == "zone_change":
            zone = kwargs.get("zone", "")
            print(f"  [MAP]  Zone: {zone}")
            self.ctrl.vibrate(3, 2)
        
        elif event_type == "cutscene_start":
            print(f"  [SCENE] Cutscene")
            self.ctrl.vibrate(2, 30)
        
        elif event_type == "cutscene_end":
            self.ctrl.stop_all()


