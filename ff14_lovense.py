class NetworkLogParser:
    """Parse FF14 ACT/IINACT network logs."""
    
    ERP_KEYWORDS = {
        "mild": ["moan", "gasp", "whimper", "pant", "shiver", "tremble", "blush"],
        "intense": ["thrust", "pound", "stroke", "grind", "ride", "bounce", "harder"],
        "climax": ["cum", "orgasm", "climax", "finish", "release"],
        "tease": ["tease", "edge", "deny", "beg", "plead", "slowly"],
        "rough": ["spank", "slap", "choke", "grab", "pull", "pin", "force"],
    }
    
    EMOTE_KEYWORDS = {
        "hug": ["hugs", "embraces"], "kiss": ["kisses"],
        "pet": ["pets", "pats"], "slap": ["slaps"],
        "spank": ["spanks"], "dance": ["dances"], "dote": ["dotes"],
    }
    
    def __init__(self, mapper):
        self.mapper = mapper
        self.player_name = None
    
    def parse_line(self, line):
        parts = line.strip().split("|")
        if len(parts) < 3:
            return
        
        opcode = parts[0]
        
        if opcode == "00" and len(parts) > 4:  # Chat
            msg = parts[4].lower() if len(parts) > 4 else ""
            for ktype, keywords in self.ERP_KEYWORDS.items():
                if any(kw in msg for kw in keywords):
                    self.mapper.handle("chat_trigger", keyword_type=ktype)
                    return
            for emote, keywords in self.EMOTE_KEYWORDS.items():
                if any(kw in msg for kw in keywords):
                    self.mapper.handle("emote", name=emote)
                    return
        
        elif opcode in ("15", "16") and len(parts) > 9:  # Ability
            try:
                target = parts[7] if len(parts) > 7 else ""
                source = parts[3] if len(parts) > 3 else ""
                damage = int(parts[9], 16) if parts[9] else 0
                if damage > 0:
                    if self.player_name and target == self.player_name:
                        self.mapper.handle("damage_taken", damage)
                    elif self.player_name and source == self.player_name:
                        self.mapper.handle("damage_dealt", damage)
            except:
                pass
        
        elif opcode == "25" and len(parts) > 3:  # Death
            if self.player_name and parts[3] == self.player_name:
                self.mapper.handle("death")
        
        elif opcode == "21" and len(parts) > 5:  # Cast start
            source = parts[3] if len(parts) > 3 else ""
            ability = parts[5] if len(parts) > 5 else ""
            if self.player_name and source != self.player_name:
                self.mapper.handle("boss_cast", name=ability)


